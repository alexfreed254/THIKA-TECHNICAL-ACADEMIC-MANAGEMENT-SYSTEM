def pdf_letterhead(doc_name, avail_width, dept_name="", meta_lines=()):
    """
    Return a list of ReportLab flowables forming the common letterhead:
    coat of arms + institute name/document title/department + institute logo.
    `avail_width` is the usable page width in points.
    """
    from reportlab.lib import colors
    from reportlab.lib.enums import TA_CENTER
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    from reportlab.lib.units import mm
    from reportlab.platypus import (
        HRFlowable, Image as RLImage, Paragraph, Spacer, Table, TableStyle,
    )

    styles = getSampleStyleSheet()
    navy = colors.HexColor("#0F2744")
    gold = colors.HexColor("#B45309")

    title_s = ParagraphStyle(
        "lh_t", parent=styles["Heading1"], fontSize=14, textColor=navy,
        alignment=TA_CENTER, spaceAfter=1, fontName="Helvetica-Bold", leading=17,
    )
    tag_s = ParagraphStyle(
        "lh_tag", parent=styles["Normal"], fontSize=7.5,
        textColor=colors.HexColor("#64748B"), alignment=TA_CENTER, spaceAfter=2,
    )
    doc_s = ParagraphStyle(
        "lh_d", parent=styles["Normal"], fontSize=11, textColor=gold,
        alignment=TA_CENTER, spaceAfter=2, fontName="Helvetica-Bold",
    )
    dept_s = ParagraphStyle(
        "lh_dep", parent=styles["Normal"], fontSize=9,
        textColor=colors.HexColor("#334155"), alignment=TA_CENTER,
        spaceAfter=1, fontName="Helvetica-Bold",
    )
    meta_s = ParagraphStyle(
        "lh_m", parent=styles["Normal"], fontSize=8,
        textColor=colors.HexColor("#475569"), alignment=TA_CENTER, spaceAfter=1,
    )

    center = [Paragraph(INSTITUTE, title_s), Paragraph(INSTITUTE_TAGLINE, tag_s),
              Paragraph(str(doc_name).upper(), doc_s)]
    if dept_name:
        center.append(Paragraph(f"Department: {dept_name}", dept_s))
    for line in meta_lines:
        if line:
            center.append(Paragraph(line, meta_s))

    side = 24 * mm

    def _img(path):
        if os.path.exists(path):
            try:
                return RLImage(path, width=19 * mm, height=19 * mm)
            except Exception:
                return ""
        return ""

    row = [[_img(COAT_PATH), center, _img(LOGO_PATH)]]
    t = Table(row, colWidths=[side, avail_width - 2 * side, side])
    t.setStyle(TableStyle([
        ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
        ("ALIGN", (0, 0), (0, 0), "CENTER"),
        ("ALIGN", (2, 0), (2, 0), "CENTER"),
        ("LEFTPADDING", (0, 0), (-1, -1), 0),
        ("RIGHTPADDING", (0, 0), (-1, -1), 0),
    ]))
    return [t, Spacer(1, 3),
            HRFlowable(width="100%", thickness=1.2, color=navy, spaceAfter=8)]


def pdf_header_style_cmds(header_row=0):
    """TableStyle commands for a light, visible header row (no dark fill)."""
    from reportlab.lib import colors
    return [
        ("BACKGROUND", (0, header_row), (-1, header_row), colors.HexColor("#DCE6F4")),
        ("TEXTCOLOR", (0, header_row), (-1, header_row), colors.HexColor("#0F2744")),
        ("FONTNAME", (0, header_row), (-1, header_row), "Helvetica-Bold"),
        ("LINEBELOW", (0, header_row), (-1, header_row), 0.8, colors.HexColor("#0F2744")),
    ]


def pdf_signature_block(avail_width, officer_title="Head of Department",
                        officer_name="", extra_officers=()):
    """
    Return flowables for the official sign-off: officer in charge, any extra
    officers, then the Chief Principal — each with Name / Sign / Date — plus
    a stamp box.
    """
    from reportlab.lib import colors
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    from reportlab.lib.units import mm
    from reportlab.platypus import Paragraph, Spacer, Table, TableStyle

    styles = getSampleStyleSheet()
    lbl_s = ParagraphStyle(
        "sig_l", parent=styles["Normal"], fontSize=8.5,
        fontName="Helvetica-Bold", textColor=colors.HexColor("#0F2744"),
        spaceAfter=2,
    )
    line_s = ParagraphStyle(
        "sig_ln", parent=styles["Normal"], fontSize=8,
        textColor=colors.HexColor("#334155"), leading=14,
    )
    stamp_s = ParagraphStyle(
        "sig_st", parent=styles["Normal"], fontSize=7.5,
        textColor=colors.HexColor("#64748B"), alignment=1,
    )

    officers = [(officer_title, officer_name)]
    officers += [(t, "") for t in extra_officers]
    officers.append((CHIEF_PRINCIPAL_TITLE, ""))

    cells = []
    for title, name in officers:
        name_part = name if name else "___________________________"
        cells.append([
            Paragraph(title.upper(), lbl_s),
            Paragraph(f"Name: {name_part}", line_s),
            Paragraph("Sign: ___________________________", line_s),
            Paragraph("Date: ___________________________", line_s),
        ])
    # Stamp box as final column
    cells.append([
        Paragraph("OFFICIAL STAMP", lbl_s),
        Spacer(1, 26),
        Paragraph("(Affix institute stamp here)", stamp_s),
    ])

    ncols = len(cells)
    gap = 4 * mm
    col_w = (avail_width - gap * (ncols - 1)) / ncols
    data = [cells]
    widths = [col_w] * ncols

    t = Table(data, colWidths=widths)
    t.setStyle(TableStyle([
        ("VALIGN", (0, 0), (-1, -1), "TOP"),
        ("BOX", (-1, 0), (-1, 0), 0.8, colors.HexColor("#94A3B8")),
        ("LEFTPADDING", (0, 0), (-1, -1), 4),
        ("RIGHTPADDING", (0, 0), (-1, -1), 4),
        ("TOPPADDING", (0, 0), (-1, -1), 4),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
    ]))
    return [Spacer(1, 14), t]
